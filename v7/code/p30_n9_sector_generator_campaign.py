def valid_names(features, names):
    out = []
    for name in names:
        group, feature = name.split(":", 1)
        if group in features and feature in features[group]:
            out.append(name)
    return out


def feature_value(features, name, key):
    group, feature = name.split(":", 1)
    return features[group][feature].get(key, 0)


def mapping_from_expression(record_keys, features, terms):
    out = {}
    for key in record_keys:
        total = 0
        for coefficient, group, feature in terms:
            total += coefficient * features[group].get(feature, {}).get(key, 0)
        out[key] = total
    return out


def compress(raw):
    palette = {value: idx for idx, value in enumerate(sorted(set(raw.values())))}
    return {key: palette[value] for key, value in raw.items()}


def colors_from_features(record_keys, features, names, extras=None):
    extras = extras or []
    return compress({
        key: (
            tuple(feature_value(features, name, key) for name in names),
            tuple(mapping[key] for mapping in extras),
        )
        for key in record_keys
    })


def atoms_from_colors(colors):
    atoms = defaultdict(list)
    for key, color in colors.items():
        atoms[color].append(key)
    return atoms


def conflict_pairs(colors, invariant):
    atoms = atoms_from_colors(colors)
    pairs = []
    for atom, keys in atoms.items():
        buckets = defaultdict(list)
        for key in keys:
            buckets[invariant[key]].append(key)
        if len(buckets) <= 1:
            continue
        for idx, left in enumerate(keys):
            for right in keys[idx + 1 :]:
                if invariant[left] != invariant[right]:
                    pairs.append((atom, left, right))
    return pairs


def conflict_summary(colors, hist_by_k, ks):
    rows = {}
    total = 0
    bad = 0
    for k in ks:
        pairs = conflict_pairs(colors, hist_by_k[k])
        rows[k] = len(pairs)
        total += len(pairs)
        bad += len({pair[0] for pair in pairs})
    return {
        "rows": rows,
        "total": total,
        "bad": bad,
        "atoms": len(atoms_from_colors(colors)),
    }


def relations(bits, n=5):
    return [
        (i, j)
        for i in range(n)
        for j in range(n)
        if i != j and has_rel(bits, n, i, j)
    ]


def covers(bits, n=5):
    out = []
    for i, j in relations(bits, n):
        if not any(
            k != i and k != j and has_rel(bits, n, i, k) and has_rel(bits, n, k, j)
            for k in range(n)
        ):
            out.append((i, j))
    return out


def minima(bits, n=5):
    return [
        i
        for i in range(n)
        if not any(has_rel(bits, n, j, i) for j in range(n) if j != i)
    ]


def maxima(bits, n=5):
    return [
        i
        for i in range(n)
        if not any(has_rel(bits, n, i, j) for j in range(n) if j != i)
    ]


def vertex_profiles(bits, n=5):
    profiles = []
    for i in range(n):
        lower = sum(1 for j in range(n) if has_rel(bits, n, j, i))
        upper = sum(1 for j in range(n) if has_rel(bits, n, i, j))
        incomparable = n - 1 - lower - upper
        profiles.append((lower, upper, incomparable))
    return tuple(sorted(profiles))


def small_deck(bits, n, child_size):
    deck = defaultdict(int)
    for subset in combinations(range(n), child_size):
        child = restrict_bits(bits, n, subset)
        deck[canon_bits(child, child_size)] += 1
    return dict(sorted(deck.items()))


def flag_descriptor(flag_key):
    bits = flag_key
    return {
        "relations": relations(bits),
        "covers": covers(bits),
        "height": height(bits, 5),
        "width": width(bits, 5),
        "intervals": interval_counts(bits, 5),
        "minima": minima(bits),
        "maxima": maxima(bits),
        "profiles": vertex_profiles(bits),
        "deck4": small_deck(bits, 5, 4),
        "deck3": small_deck(bits, 5, 3),
    }


def feature_mapping(features, name):
    group, feature = name.split(":", 1)
