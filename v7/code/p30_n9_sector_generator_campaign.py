def mask_for_pairs(mapping, pairs):
    mask = 0
    for idx, (_atom, left, right) in enumerate(pairs):
        if mapping[left] != mapping[right]:
            mask |= 1 << idx
    return mask


def exact_min_cover(candidates, masks, full_mask, max_size=5):
    active = [name for name in candidates if masks[name]]
    union = 0
    for name in active:
        union |= masks[name]
    if union != full_mask:
        return None
    active.sort(key=lambda name: masks[name].bit_count(), reverse=True)

    for size in range(1, max_size + 1):
        best_possible = sorted((masks[name].bit_count() for name in active), reverse=True)[:size]
        if sum(best_possible) < full_mask.bit_count():
            continue
        for combo in combinations(active, size):
            cover = 0
            for name in combo:
                cover |= masks[name]
            if cover == full_mask:
                return combo
    return None


def greedy_sequence(base_names, extras, selected, record_keys, features, hist_by_k, ks):
    rows = []
