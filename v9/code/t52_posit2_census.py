def longest_chain(C, alive):
    idx = np.nonzero(alive)[0]
    if len(idx) == 0: return []
    sub = C[np.ix_(idx, idx)]
    n = len(idx)
    indeg = sub.sum(axis=0).astype(int)
    Lv = np.ones(n, dtype=int); par = -np.ones(n, dtype=int)
    stack = [i for i in range(n) if indeg[i] == 0]
    ind = indeg.copy()
    succ = [np.nonzero(sub[i])[0] for i in range(n)]
    while stack:
        v = stack.pop()
        for w_ in succ[v]:
            if Lv[v] + 1 > Lv[w_]:
                Lv[w_] = Lv[v] + 1; par[w_] = v
            ind[w_] -= 1
            if ind[w_] == 0: stack.append(int(w_))
    end = int(np.argmax(Lv)); ch = []
    v = end
    while v != -1:
        ch.append(int(idx[v])); v = int(par[v])
    return ch[::-1]

def chains_of(C, minlen=4):
    alive = np.ones(C.shape[0], dtype=bool)
    out = []
    while True:
        ch = longest_chain(C, alive)
        if len(ch) < minlen: break
        out.append(ch); alive[ch] = False
    return out

def feasible(C, chains, chi):
    """Bellman-Ford feasibility of the offset system."""
    Mc = len(chains)
    of = {}
    for ci, ch in enumerate(chains):
        for e in ch: of[e] = ci
    INF = float("inf")
    W = {}
    for ci in range(Mc):
        for cj in range(Mc):
            if ci == cj: continue
            W[(ci, cj)] = INF
    ii, jj = np.nonzero(C)
    for x, y in zip(ii, jj):
        if x in of and y in of and of[x] != of[y]:
            a, b = of[x], of[y]
            w = chi[y] - chi[x] - 1e-9
            if w < W[(a, b)]: W[(a, b)] = w
    dist = [0.0] * Mc
    for it in range(Mc):
        upd = False
        for (a, b), w in W.items():
            if w < INF and dist[a] + w < dist[b] - 1e-15:
                dist[b] = dist[a] + w; upd = True
        if not upd: return True
    return not upd

def grow_A1(n, K=12):
    M0 = 8
    def dS0(Ct, Ctf, D):
        if not D.any(): return 0.0
        Didx = np.nonzero(D)[0]
        Mm = (Ct[Didx, :] & D[None, :]).astype(np.float32)
        k = Mm.sum(1)
        edges = ((Mm @ Ctf) * Mm).sum(1)
        r = 2.0 * edges / np.maximum(k * (k - 1), 1.0)
        return float(np.where(k >= M0, (r - 0.5) ** 2, 0.25).sum())
    def sc(nr, m):
        if m < 2: return 0.0
        r = 2.0 * nr / (m * (m - 1))
        return (m * (m - 1) / 2) * (r - 0.5) ** 2
    C = np.zeros((n, n), dtype=bool); nrel = 0
    for t in range(1, n):
        Ct = C[:t, :t]; Ctf = Ct.astype(np.float32)
        cands = [np.zeros(t, dtype=bool)]
        while len(cands) < K + 1:
            p = float(np.exp(rng.uniform(np.log(min(1.0 / t, 0.59)), np.log(0.6))))
            anc = rng.random(t) < p
            D = anc | Ct[:, anc].any(axis=1) if anc.any() else anc
            cands.append(D)
        dS = [dS0(Ct, Ctf, D) + sc(nrel + int(D.sum()), t + 1) - sc(nrel, t) for D in cands]
        pick = int(np.argmin(dS))
        C[:t, t] = cands[pick]; nrel += int(cands[pick].sum())
    return C

