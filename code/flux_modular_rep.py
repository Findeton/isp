"""
Paper 43 §7.1 -- Phase 0: pin the target modular space for Lemma 1(b).

The 't Hooft modular action on Z_2 x Z_2 fluxes (e,m) is (mod 2):
    S: (e,m) -> (m, -e) = (m, e)        [electric <-> magnetic swap]
    T: (e,m) -> (e, m + e)
acting on the 4-dim flux space V = {f : Z_2 x Z_2 -> C}. We compute the SL(2,Z) representation these
generate, verify the group relations, identify the congruence subgroup Gamma that acts trivially
(=> the partition vector is a modular form for Gamma), and decompose the representation. This pins the
finite-dimensional target space the conjecture's part (a) lives in.
"""
import numpy as np
import itertools

# flux states (e,m): index 0..3
states = [(0, 0), (0, 1), (1, 0), (1, 1)]
idx = {s: i for i, s in enumerate(states)}


def perm_matrix(action):
    """4x4 permutation matrix M with M[a, b] = 1 if action(state_b) = state_a (push-forward on f)."""
    M = np.zeros((4, 4), dtype=int)
    for b, s in enumerate(states):
        a = idx[action(s)]
        M[a, b] = 1
    return M


S = perm_matrix(lambda em: (em[1], em[0]))                       # (e,m)->(m,e)
T = perm_matrix(lambda em: (em[0], (em[1] + em[0]) % 2))         # (e,m)->(e,m+e)
I = np.eye(4, dtype=int)


def matpow(M, k):
    R = I.copy()
    for _ in range(k):
        R = R @ M
    return R


def generate_group(gens):
    seen = {tuple(I.flatten())}
    frontier = [I]
    elems = [I]
    while frontier:
        nf = []
        for g in frontier:
            for h in gens:
                gh = g @ h
                key = tuple(gh.flatten())
                if key not in seen:
                    seen.add(key); nf.append(gh); elems.append(gh)
        frontier = nf
    return elems


