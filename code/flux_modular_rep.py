    print(f"   T^2 = I ? {np.array_equal(matpow(T,2), I)}    (T^2 in Gamma(2))")
    print(f"   (ST)^3 = I ? {np.array_equal(matpow(S@T,3), I)}")
    G = generate_group([S, T])
