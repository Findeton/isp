"""
v6 Paper 2 Part II §5.37: invariant-law guess search.

The previous audit says that known construction classes fail unless the full
four-role Gram is supplied.  This script asks a sharper first-principles
question:

    What invariant scalar law could make record, source, causal, and
    antichain-density roles four derivatives of the same fact?

The surviving guess is an invariant deletion action:

    A_x = log dP(local collar with x retained) / dP(local collar with x deleted)

or equivalently a finite relative-entropy/modular deletion increment.  If the
four roles are generated by invariant deformations of this one scalar action,
then the role Gram is its local Fisher/Hessian matrix.  That is a real Branch-A
target; it is not proved here for the actual interacting ISP law.
"""

from __future__ import annotations

from dataclasses import dataclass

from v6_p2r_full_role_gram_derivation import (
    ROLE_ORDER,
    LawInstance,
    base_role_rows,
    beta_from_gram,
    gram,
    matrix_span,
    role_rows,
)


@dataclass
class Guess:
    name: str
    invariant_scalar: bool
    deletion_atom: bool
    record: bool
    source: bool
    causal: bool
    antichain: bool
    same_functional: bool
    local_microcausal: str
    beta_status: str
    verdict: str


@dataclass
class HessianAudit:
    candidate: str
    full_roles: bool
    same_scalar: bool
    acv: bool
    j_span: float
    kappa_min: float
    beta_span: float
    verdict: str


def stable_full_hessians() -> list[list[list[float]]]:
    out = []
    for step in [1.0, 0.5, 0.25, 0.0]:
        rows = {
            "record": [0.95 + 0.02 * step, 0.05, 0.02, 0.00],
            "source": [0.08, 0.92 + 0.03 * step, 0.05, 0.02],
            "causal": [0.03, 0.06, 0.96 + 0.01 * step, 0.04],
            "antichain": [0.02, 0.04, 0.08, 0.98 + 0.01 * step],
        }
        out.append(gram(role_rows(LawInstance(rows, "pre"), ROLE_ORDER)))
    return out


def no_source_hessians() -> list[list[list[float]]]:
    out = []
    for strength in [0.55, 0.75, 0.95, 1.15]:
        rows = base_role_rows(strength)
        out.append(gram(role_rows(LawInstance(rows, "pre"), ROLE_ORDER)))
    return out


def audit_hessian(
    candidate: str,
    hessians: list[list[list[float]]],
    full_roles: bool,
    same_scalar: bool,
) -> HessianAudit:
    kappas = []
    betas = []
    oks = []
    if full_roles and same_scalar:
        for hessian in hessians:
            kappa, beta, ok = beta_from_gram(hessian, 0.015)
            kappas.append(kappa)
            betas.append(beta)
            oks.append(ok)

    acv = all(oks) if oks else False
    j_span = matrix_span(hessians) if hessians else float("inf")
    kappa_min = min(kappas) if kappas else 0.0
    beta_span = max(betas) - min(betas) if betas else float("inf")
    passes = (
        full_roles
        and same_scalar
        and acv
        and kappa_min > 0.0
        and beta_span <= 0.02
        and j_span <= 0.07
    )
    return HessianAudit(
        candidate=candidate,
        full_roles=full_roles,
        same_scalar=same_scalar,
        acv=acv,
        j_span=j_span,
        kappa_min=kappa_min,
        beta_span=beta_span,
        verdict="PASS-TARGET" if passes else "FAIL",
    )


