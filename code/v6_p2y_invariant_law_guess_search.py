def fmt(value: float) -> str:
    return "inf" if value == float("inf") else f"{value:.4f}"


def yn(flag: bool) -> str:
    return "yes" if flag else "no"


def print_guesses(rows: list[Guess]) -> None:
    print("first-principles guess audit")
    print("----------------------------")
    print(
        "candidate                       scalar  delete  rec  src  caus  anti  "
        "sameF  locality                    beta/status       verdict"
    )
    for row in rows:
        print(
            f"{row.name:31s}  "
            f"{yn(row.invariant_scalar):6s}  "
            f"{yn(row.deletion_atom):6s}  "
            f"{yn(row.record):3s}  "
            f"{yn(row.source):3s}  "
            f"{yn(row.causal):4s}  "
            f"{yn(row.antichain):4s}  "
            f"{yn(row.same_functional):5s}  "
            f"{row.local_microcausal:26s}  "
            f"{row.beta_status:16s}  "
            f"{row.verdict}"
        )
    print()


def print_hessian_audits(rows: list[HessianAudit]) -> None:
    print("single-scalar Hessian audit")
    print("---------------------------")
    print(
        "candidate                       roles  sameF  ACV   J_span  "
        "kappa_min  beta_span  verdict"
    )
    for row in rows:
        print(
            f"{row.candidate:31s}  "
            f"{yn(row.full_roles):5s}  "
            f"{yn(row.same_scalar):5s}  "
            f"{('PASS' if row.acv else 'FAIL'):4s}  "
            f"{fmt(row.j_span):>7s}  "
            f"{row.kappa_min:9.4f}  "
            f"{fmt(row.beta_span):>9s}  "
            f"{row.verdict}"
        )
    print()


